# SQL скрипт для добавления поля codeforces_karma в таблицу users
# Выполнить в SurrealDB CLI или через GUI

-- Добавляем поле codeforces_karma со значением по умолчанию 0
-- Поле будет автоматически добавлено ко всем записям при первом обращении
-- Но можно обновить все записи явно:

UPDATE users SET codeforces_karma = 0 WHERE codeforces_karma IS NONE;

-- Примечание: SurrealDB поддерживает динамические поля,
-- поэтому поле codeforces_karma может быть записано в любую запись
-- без предварительного объявления схемы.
-- Этот скрипт просто устанавливает значение по умолчанию для всех существующих пользователей.
